package Challenge7Arithmetic;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println();

        double x = 5;
        double y = 3;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);

    }
}
